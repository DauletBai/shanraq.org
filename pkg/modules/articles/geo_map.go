// RegionCounts returns the number of published listings per region text.
func (s *ListingStore) RegionCounts(ctx context.Context) (map[string]int, error) {
	rows, err := s.db.Query(ctx, `
		SELECT region, COUNT(*) FROM listings
		WHERE status = 'published' AND region <> ''
		GROUP BY region`)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	out := map[string]int{}
	for rows.Next() {
		var region string
		var n int
		if err := rows.Scan(&region, &n); err != nil {
			return nil, err
		}
		out[region] = n
	}
	return out, rows.Err()
}
