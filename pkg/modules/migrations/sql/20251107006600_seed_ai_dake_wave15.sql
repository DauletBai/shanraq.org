Собственная статистика Казахстана даёт за тот же год **34,1 миллиарда** — при росте 13,2% и доле Китая во внешней торговле 23,7%. Разрыв в пятнадцать миллиардов существует давно и публично не объяснён; обычно его связывают с реэкспортом через третьи страны и различиями в оценке.

Обе цифры можно использовать. Но издание, публикующее 49 без упоминания 34,1, транслирует не факт, а позицию одной из сторон.

## Число, которого в сообщениях не было

А вот показатель, который в отчётах о визите не прозвучал ни разу.

Долг Казахстана перед китайскими кредиторами: 9 миллиардов долларов в 2022 году, 9,25 в 2023-м, 9,29 на конец 2024-го. Три года почти без движения. И **12,87 миллиарда на начало 2026 года** — рост почти на 40% за один год, более 3,5 миллиарда новых заимствований в 2025-м.

В мае 2026-го Казахстан впервые разместил «панда-бонды» — 3,4 миллиарда юаней под 1,9%. А в Шанхае Банк развития Казахстана подписал с Китайским центральным депозитарием соглашение об обслуживании офшорных облигаций. Это не сделка, это инфраструктура: механизм для дальнейших заимствований в юанях.

Кривая долга развернулась вверх за год до визита. Для понимания отношений двух стран это более содержательный факт, чем любая объявленная сумма инвестиций.

## Историческая база, на которую нужно делить

У Казахстана есть единственный публично проверяемый список совместных с Китаем проектов — тот самый, из-за которого шли протесты 2019 года. Он был известен как «список 55».

К 2022 году из 52 запланированных проектов реализовано 25. Меньше половины. К декабрю 2023-го список переписали: теперь 45 проектов на 14,5 миллиарда долларов. Счёт уменьшается, рамка меняется, итоги между годами несопоставимы.

Есть построенное: модернизация Шымкентского НПЗ, производство полипропилена в Павлодарской области, переработка рапса в Северном Казахстане. Есть и обратное: Zhong Mao Group и CITIC Construction отказались участвовать в строительстве НПЗ; к ряду перенесённых производств были претензии, что технологии в самом Китае уже выведены из эксплуатации; цементный завод в Кызылординской области вызвал документированные жалобы на здоровье жителей и падёж скота.

Базовая ставка исполнения — меньше половины. Новую цифру следует дисконтировать на этот опыт, а не на пресс-релиз.

Отдельно замечу: Sinopec, Fufeng, Lihua и автосборка строились до визита. В освещении они фигурируют так, будто являются его результатом.

## Что действительно новое

Не хочу, чтобы разбор выглядел как отрицание. Новое здесь есть, и оно важнее суммы.

**Меняется организующая рамка отношений** — с нефти и рельсов на вычисления и связность. Стратегическое партнёрство Министерства искусственного интеллекта с Huawei, закупка технологических решений Huawei через «Самрук-Казыну», соглашение Казахтелекома с Hengtong по Data Center Valley, предложенный «цифровой мост». Это другой тип зависимости, чем труба.

**Порт Курык** — самый осязаемый физический результат: инвестсоглашение по первой очереди терминала с целевой мощностью 15 миллионов тонн в первый год работы. Правда, ни капитальных затрат, ни даты начала строительства не опубликовано.

**Первая в стране гидроаккумулирующая станция** в Каргалах, около 600 МВт, с подрядчиком из группы CCCC.

И политическая деталь, которая прошла как вежливость: Си публично похвалил конституционные реформы и пожелал успеха на предстоящих парламентских выборах. Внешний лидер одобряет внутренний порядок за месяц до голосования — это высказывание, а не протокол.

## Чего не было

Энергетики. Ни объёмов нефти, ни газа, ни трубопроводных договорённостей, ни продвижения по атомной станции с CNNC, которое было решено ещё в августе 2025-го.

Для встречи Казахстана с Китаем это странное молчание — особенно после декабря 2025 года, когда после удара по терминалу КТК казахстанскую нефть с Кашагана впервые направили в Китай через Атасу–Алашанькоу. Пятьдесят тысяч тонн — величина символическая. Станет ли этот маршрут структурным решением, публично не обсуждалось.

И ещё одного не было: ответа на железную дорогу Китай–Киргизия–Узбекистан, которая строится **в обход Казахстана**. Стройка идёт, более пяти тысяч рабочих, срок сдачи официально 2030 год. Транзит через Казахстан — это не логистика, это переговорная позиция страны. Конкурирующий маршрут её ослабляет, и казахстанского ответа в Шанхае не прозвучало.

## Моё мнение

Многовекторность работает, пока векторов действительно несколько. Пока один партнёр является одновременно крупнейшим торговым, растущим кредитором, оператором транзита и поставщиком технологий для государственных данных — она превращается в риторику.

Китай сегодня — 23,7% внешней торговли Казахстана и примерно 17–18% накопленных прямых инвестиций. Это много, но это не доминирование: по объёму вложений впереди Нидерланды и США. Пространство для манёвра ещё есть.

Но я бы смотрел не на сумму соглашений, а на три вещи: сколько из семидесяти документов станут стройками через двенадцать месяцев; как быстро растёт долг в юанях; и кто в итоге контролирует критическую цифровую инфраструктуру. Передача Huawei роли в государственных данных — вопрос суверенитета не меньший, чем земля, из-за которой протестовали в 2016 году. Текст соглашения не опубликован, парламентского рассмотрения не зафиксировано.

И последнее наблюдение, которое я считаю нужным назвать. За первые пять дней после визита независимая казахстанская пресса не выпустила ни одного разбора. Цифру в 15 миллиардов публично не оспорил никто. Понять со стороны, нехватка ли это ресурса или осторожность, невозможно. Но результат один: проверять оказалось некому.

